#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t	t;

	t = 0;
	while (src[t])
	{
		dest[t] = src[t];
		++t;
	}
	dest[t] = '\0';
	return (dest);
}
